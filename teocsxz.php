    <div class="texto1"><h1>- Se escriben con C, los verbos terminados en cir y ducir.<br>
Excepción: asir.<br>
Ejemplos: conducir, aducir, traducir, esparcir, producir, relucir, zurcir, decir.<br>
- Se escriben con C, las palabras terminadas en ancia, ancio, encía.<br><br>
Excepciones: ansia, Hortensia.<br>
Ejemplos: constancia, excelencia, extravagancia, cansancio, decadencia, indulgencia, fragancia, conciencia, distancia.<br>
- Se escriben con C, las palabras terminadas en ción, afines a to, tor, dar.<br><br>
Ejemplos: composición – compositor, bendición – bendito, rotación – rotador, atribución – atributo, sensación – sensato, admiración – admirador, distribución – distribuidor.<br>
- Se escriben con C, los diminutivos: cito, ecito, ecillo, si proceden de palabras sin S final.<br><br>
Ejemplos: pez – pececito, dulce – dulcecito, pie – piececito, flor – florecita, mamá – mamacita.<br>
- Se escriben con C, los sufijos cida, cido, cidio.<br><br>
Ejemplos: homicida, parricida, amanecido, aparecida, establecido, infanticida, genocidio.<br>
- Se escriben con C, las palabras terminadas en cimiento.<br><br>
Ejemplos: agradecimiento, padecimiento, restablecimiento, nacimiento, establecimiento, enriquecimiento, yacimiento.<br>
- Se escriben con C, los verbos terminados en cer.<br><br>
Excepciones: toser, coser, ser.<br>
Ejemplos: nacer, yacer, hacer, adolecer, agradecer, retorcer, estremecer.<br>
- Se escriben con C, los verbos terminados en ciar.<br><br>
Excepciones: lisiar, ansiar, extasiar, anestesiar.<br>
Ejemplos: apreciar, acariciar, neciar, vaciar, negociar, viciar.<br>
- Se escriben con C, las palabras terminadas en acia, icia, icie, icio.<br><br>
Excepciones: Dionisio, gimnasio, Asia, anastasia, alisio, eutanasia.<br>
Ejemplos: Fenicio, planicie, bullicio, pericia, codicia, malicia, falacia.<br>
- Se escribe con C, la terminación ces que surge del plural de las palabras que contienen z.<br><br>
Ejemplos: maíz-maíces, raíz-raíces, pez-peces, rapaz-rapaces, atroz-atroces.<br>
- Se escriben con C, las formas de los verbo terminados en ceder, cender, cibir, citar, siempre que no proceda de raíces que lleven S.<br><br>
Ejemplos: conceder, encender, recitar, recibir, anteceder, percibir.<br>
- Se escriben con C, los verbos terminados en zar ante la vocal e cambian a c.<br><br>
Ejemplos: Analizar – Analice, avergonzar-avergüence, canalizar – canalice, utilizar – utilice<br>
- Se escriben con S, las palabras que terminan en ense, referente a los gentilicios.<br><br>
Ejemplos: nicaragüense, costarricense, canadiense, veragüense, colonense.<br>
- Se escriben con S, las terminaciones sivo, siva.<br><br>
Ejemplos: corrosivo, masiva, explosivo, expresiva, intensivo.<br>
- Se escriben con S, las palabras terminadas en sión, cuando proceden de palabras terminadas en so, sor, sivo.<br><br>
Ejemplos: comprensión – comprensivo, persuasión – persuasivo, represión – represivo, previsión – previsor, precisión – preciso.<br>
- Se escriben con S, las palabras terminadas en los superlativos isimo, isima.<br><br>
Ejemplos: bellísima, lindísimo, feísima, inteligentísima, delicadísima.<br>
- Se escriben con S, las palabras terminadas en oso, osa.<br><br>
Ejemplos: Bondadoso, sabrosa, dadivoso, perezosa, maravilloso, grandioso.<br>
- Se escriben con S, las palabras terminadas en ismo.<br><br>
Ejemplos: espiritismo, oscurantismo, atletismo, altruismo, vanguardismo.<br>
- Se escriben con S, las palabras terminadas en esca, esco.<br><br>
Ejemplos: grotesca, dantesca, burlesco, gigantesco, pintoresco.<br>
- Se escribe con S, la variante pronominal se, en forma proclítica o enclítica.<br><br>
Ejemplos: enclítica – tomóse, llevarse.<br>
proclítica – se acercó, se marcha.<br>
- Se escribe con S, a terminación ese del verbo auxiliar haber, pretérito pluscuamperfecto, modo subjuntivo.<br><br>
Ejemplos: hubiese amado, hubiese nadado, hubiese temido.<br>
- Se escriben con S, las terminaciones esta, esto, ista.<br><br>
Ejemplos: feminista, violinista, tiesto, fiesta artista.<br>
- Se escribe con S, la terminación se de todos los verbos del pretérito imperfecto, del modo subjuntivo.<br><br>
Ejemplos: partiese, amase, cumpliese, subiese, temiese.<br>
- Se escriben con S, las terminaciones ersa, erse, erso.<br><br>
Ejemplos: atreverse, adversa, converso, inverso, embellecerse, perversa.<br>
- Se escriben con S, los verbos que en su infinitivo no tienen s, c, z.<br><br>
Ejemplos: querer – quiso, haber- has, ver – ves.<br>
- Se escriben con Z, las palabras terminadas en anza/o y azgo.<br><br>
Excepciones: gansa/o, mansa/o.<br>
Ejemplos: adivinanza, mudanza, panza, hallazgo, danza, alabanza, almirantazgo.<br>
- Se escriben con Z, las terminaciones ez, eza, az, oz, de los nombres abstractos.<br><br>
Ejemplos: belleza, voraz, pereza, fugaz, rigidez, atroz, palidez, paz, torpeza, rapaz, timidez, eficaz.<br>
- Se escriben con Z, las terminaciones azo, aza que denotan aumento, golpe.<br><br>
Ejemplos: manaza, carrazo, ojazos, codazo, puertazo, mujeraza<br>
- Se escriben con Z, las terminaciones iz, ez, oz, az, de los nombres patronímicos.<br><br>
Ejemplos: Rodríguez, Ruiz, Sánchez, Muñoz, Ramírez, Ortiz, Villalaz.<br>
- Se escriben con Z, las terminaciones zuela, zuelo, que denotan disminución o desprecio.<br><br>
Ejemplos: mujerzuela, ladronzuelo, portezuela, jovenzuelo.<br>
- Se escriben con Z, algunos verbos en infinitivo terminados en zar y sus conjugaciones delante de las vocales a, o.<br><br>
Ejemplos: analizar – analizo / a, paralizar – paralizo / a, aterrorizar – aterrorizo / a, canalizar – canalizo / a.<br>
- Se escriben con Z, las palabras terminadas en izo, iza.<br><br>
Ejemplos: mestiza, cobrizo, plomizo, movediza, enfermiza.<br>
- Se escriben con X, los compuestos con las preposiciones latinas ex, extra.<br><br>
Ejemplos: extravagante, extraordinario, exdirector, expresidente, exalcade, exalumno.<br>
- Se escribe X cuando percibimos el sonido cs o gs.<br><br>
Excepciones: facsímil, fucsina, fucsia.<br>
Ejemplos: sexo, conexión, próximo, oxígeno, exacto, axioma.<br>
- Se escribe X, cuando tengamos este sonido seguido de h o vocal.<br><br>
Ejemplos: exhalar, exasperar, exhausto, exhortar, exhumar, exigente, exuberante.<br>- Se escribe X, delante de las silabas pla, ple, pli, plo, pre, pri, pro.<br><br>
Ejemplos: explanada, explosivo, explicación, explorador, expreso, expropiar.<br>
- Se escribe con X, el prefijo hexa que significa seis.<br><br>
Ejemplos: hexágono, hexagonal, hexámetro, hexasílaba, hexapétalo, hexaedro.<br>
